package com.recruit.entity;

public class AdminData {
    private int id;
    private String name;
    private String password;
    AdminData(){

    }
    public void  setData(int id,String name,String password){
        this.id=id;
        this.name=name;
        this.password=password;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPassword() {
        return password;
    }
}
